import { writeFileSync } from "node:fs";
import postgres from "postgres";

const selectedTripIds = [
  "dd90e32d-e72f-4605-b388-4b4319a7787d",
  "9c3e15ea-4a49-4984-8ff3-d0b0a34d2947",
  "9b46fac1-8472-4cd4-9529-0750472d7cdd",
];
const sql = postgres(process.env.DATABASE_URL, { max: 1 });
const parse = (value, fallback) => {
  try {
    return value ? JSON.parse(value) : fallback;
  } catch {
    return fallback;
  }
};

try {
  const tripRows = await sql`
    select * from trips where id = any(${selectedTripIds})
  `;
  const dayRows = await sql`
    select * from trip_days
    where trip_id = any(${selectedTripIds})
    order by trip_id, day_index
  `;
  const itemRows = await sql`
    select * from itinerary_items
    where trip_id = any(${selectedTripIds})
    order by trip_id, day_id, position
  `;
  const jobRows = await sql`
    select * from ai_jobs
    where trip_id = any(${selectedTripIds})
      and status = 'completed'
    order by created_at
  `;
  const order = new Map(selectedTripIds.map((id, index) => [id, index]));
  const seeds = tripRows
    .sort((a, b) => order.get(a.id) - order.get(b.id))
    .map((trip) => ({
      id: trip.id,
      title: trip.title,
      destination: trip.destination,
      startDate: trip.start_date,
      endDate: trip.end_date,
      status: trip.status,
      revision: trip.revision,
      currency: trip.currency,
      budgetTotal: trip.budget_total,
      constraints: parse(trip.constraints_json, {}),
      createdAt: new Date(trip.created_at).toISOString(),
      days: dayRows
        .filter((day) => day.trip_id === trip.id)
        .map((day) => ({
          id: day.id,
          dayIndex: day.day_index,
          date: day.date,
          title: day.title,
          weather: parse(day.weather_json, null),
          summary: parse(day.summary_json, {}),
          items: itemRows
            .filter((item) => item.day_id === day.id)
            .map((item) => ({
              id: item.id,
              dayId: item.day_id,
              type: item.type,
              title: item.title,
              startTime: item.start_time,
              durationMinutes: item.duration_minutes,
              position: item.position,
              notes: item.notes,
              locked: item.locked,
              lockTime: item.lock_time,
              sourceType: item.source_type,
              cost: item.cost,
              metadata: parse(item.metadata_json, {}),
            })),
          routes: [],
        })),
    }));
  const jobs = jobRows
    .map((job) => {
      const context = parse(job.context_json, {});
      return {
        id: job.id,
        tripId: job.trip_id,
        prompt: job.prompt,
        mode: context.mode || "conversation",
        status: "completed",
        stage: "completed",
        progress: 100,
        attempts: job.attempts,
        activity: [
          {
            kind: "assistant",
            status: "completed",
            title: "已读取你的需求和当前行程",
          },
          ...(context.events || []),
        ],
        createdAt: new Date(job.created_at).toISOString(),
        updatedAt: new Date(job.updated_at).toISOString(),
        result: parse(job.result_json, null),
        error: job.error,
      };
    })
    .filter((job) => job.mode === "conversation" && job.result?.message);
  const source = `// Generated by scripts/export-demo-seeds.mjs\nexport const demoTripSeeds = ${JSON.stringify(seeds, null, 2)};\n\nexport const demoJobSeeds = ${JSON.stringify(jobs, null, 2)};\n`;
  writeFileSync("lib/demo-seeds.generated.ts", source, "utf8");
  console.log(
    `Exported ${seeds.length} trips and ${jobs.length} conversations to lib/demo-seeds.generated.ts`,
  );
} finally {
  await sql.end();
}
